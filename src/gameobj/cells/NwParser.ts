    public static parseCell (id : string, cellFile: string, cellProperties: {[name:string]:any} = {}): T.Cell {
      const cellData = NwParser.createCellData(cellFile)
      const cellTiles = NwParser.createTiles(cellData)
      const npcs = NwParser.extractNPCS(cellFile)
      return {
        // TODO append uid
        id: 'cell::0::levels::0::'+id+"/"+Identification.newID(),
        tileSize: 16,
        tileWidth: 64,
        tileHeight: 64,
        tiles: cellTiles,
        tileset: 'tileset',
        npcs: npcs,
        x: cellProperties['x'] || 0,
        y: cellProperties['y'] || 0,
        file: cellFile,
        // properties: cellProperties
      };
    }

    private static extractNPCS(cellFile: string): {[name:string]:T.gameobject} {
      let npcs : {[name:string]:T.gameobject} = {}
      const cellLines : Array<string> = cellFile.split('\n')
      for(let x in cellLines){
        if(cellLines[x].startsWith("NPC ")){
          // while cellLines[x++] ! startsWith"NPC", script.push(x+'\n')
          let script : string = ''
          let scriptLine : number = Number(x)+1
          while(!cellLines[scriptLine].startsWith("NPC ") && scriptLine < cellLines.length - 1){
            script += cellLines[scriptLine]+'\n'
            scriptLine+=1
          }
          // 
          const npcProps : Array<string> = cellLines[x].split(' ')
          const newID : string = Identification.newID()
          const npc : T.gameobject = {
            // TODO append uid
            id: 'npc::1::nwnpcs::0::'+npcProps[1]+"::"+newID,
            file: npcProps[1],
            x: Number(npcProps[2])*16,
            y: Number(npcProps[3])*16,
            scale: new Float32Array([1.,1.]),
            angle: 0,
            texture : undefined,
            // properties: {script:script}
          }
          npcs[newID] = npc
        }
      }
      return npcs
    }
  
    public static createTiles = (cellData: Array<Array<string>>) => {
      let cellTiles = new Array<T.cellTile>();
      
      let headerOffset : number = 1;
      for (var y=0; y<64; y++) {
        for (var x=0; x<64; x++) {
            const tileStr = cellData[y + headerOffset][5].charAt(x*2) + 
                            cellData[y + headerOffset][5].charAt(2*x+1)
            const tile = NwParser.tileSwitch[tileStr]
            cellTiles.push({
              x: x,
              y: y,
              frameX: tile[0], 
              frameY: tile[1],
              id: tile[2]
            })
        }
      }

      return cellTiles;
    }
  
    public static createCellData = (cellFile: string) => {
      const cellArray = cellFile.split('BOARD');
      const cellTiles = new Array<Array<string>>();
  
      for (let i = 1; i < 65; i++) {
        cellTiles[i] = cellArray[i].split(' ');
      }
  
      return cellTiles;
    }


